/**
 * Create a custom color palette
 * @param {string} name - Name of the palette
 * @param {Array} colors - Array of color objects with name and color properties
 * @returns {Object} Palette object
 */
export function createCustomPalette(name, colors) {
  return {
    name,
    colors: colors.map(color => ({
      name: color.name || 'unnamed',
      color: color.color
    }))
  }
}

/**
 * Get a random color from a palette
 * @param {Array} palette - Color palette array
 * @returns {Object} Random color object
 */
export function getRandomColor(palette) {
  return palette[Math.floor(Math.random() * palette.length)]
}

/**
 * Convert hex color to RGB array
 * @param {string} hex - Hex color string (e.g., '#ff0000')
 * @returns {Array} RGB array [r, g, b]
 */
export function hexToRgb(hex) {
  const result = /^#?([a-f\d]{2})([a-f\d]{2})([a-f\d]{2})$/i.exec(hex)
  return result ? [
    parseInt(result[1], 16),
    parseInt(result[2], 16),
    parseInt(result[3], 16)
  ] : null
}

/**
 * Convert RGB array to hex string
 * @param {Array} rgb - RGB array [r, g, b]
 * @returns {string} Hex color string
 */
export function rgbToHex(rgb) {
  return '#' + rgb.map(x => {
    const hex = x.toString(16)
    return hex.length === 1 ? '0' + hex : hex
  }).join('')
}